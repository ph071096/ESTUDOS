# Essa parte está apenas atribuindo o valor 1 à variável chamada 'naruto'.
# Se precisar de uma explicação mais detalhada ou de uma modificação, por favor especifique.
# Aqui estamos inicializando a variável 'naruto' com o valor 0 e a variável 'limite' com o valor 100.
# Em seguida, usamos um laço while para imprimir o valor de 'naruto' enquanto ele for menor ou igual a 100.
# A cada iteração, incrementamos 'naruto' em 1.
naruto = 0
limite = 100

while naruto <= limite:
    print(f'Contador: {naruto}')
    naruto += 1